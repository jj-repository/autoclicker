        for row in rows:
            if isinstance(row, QHBoxLayout):
                vbox.addLayout(row)
            elif isinstance(row, QWidget):
                vbox.addWidget(row)
        box.setLayout(vbox)
        if tooltip:
            box.setTitle(f"{title}  \u24d8")
            box.setToolTip(tooltip)
        return box

