    def get_key_display_name(self, key) -> str:
        if hasattr(key, "name"):
            name = key.name
            if name.startswith("f") and name[1:].isdigit():
                return name.upper()
            return name.capitalize()
        elif hasattr(key, "char") and key.char:
            return key.char.upper()
        return str(key)

